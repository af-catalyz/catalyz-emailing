    <font face="Arial" style="line-height: 14px; font-size: 12px;font-weight: bold;" size="2" color="#000000">
    		Vous étiez près de 200 convives à  vivre le 8ème anniversaire de Kreactiv’, vendredi 13 mai 2011. Une  soirée qui a rimé avec convivialité, dans une ambiance jazzy. L’équipe de  Kreactiv’ a pris soin des papilles de ses clients, partenaires et amis… comme  de leurs yeux grâce au cadre cosy du Château Saint-Louis. L’événement, placé  sous le signe de la chance, a été ponctué d’animations surprenantes et  ludiques.<br /><br />
		</font>
    <font face="Arial" style="line-height: 14px; font-size: 12px;" size="2" color="#000000">
    		Le  discours d’Emmanuelle Tandonnet était l’occasion d’ancrer plus que jamais le  positionnement de l’agence&nbsp;: «&nbsp;<em>Nos  clients nous considèrent comme leur service communication externalisé, tant la  proximité est forte dans notre démarche d’accompagnement de leur développement</em> ». Avec une vocation claire de conseil en communication, Kreactiv’ a la chance  de gérer l’image de marque de 400 entreprises, dont une grande majorité est  innovante par la démarche ou l’offre. De l’identité d’entreprise au webmarketing, en passant par la communication  imprimée et web, le panel des prestations de l’agence est désormais étoffé  d’une nouvelle offre&nbsp;: l’organisation événementielle.
				<br />
				L’agence  écrit une histoire collective depuis 8 années grâce à une équipe soudée et très  complémentaire. «&nbsp;<em>C’est la  personnalité de Kreactiv’ qui en fait une agence pas comme les autres. Chacun  d’entre nous partage la même passion du métier, cultive la sincérité des  relations, vise l’excellence et ... reste modeste&nbsp;!</em>&nbsp;». Les  qualités de l’agence auront été par la suite présentées par 3 clients, qui ont  témoigné devant l’assemblée.<br /><br /><br />
		</font>
